        Regarding the front-end development process, I'm going to focus on putting the game parts in motion.
        The goal was to enable for the user dragging the Home page's blue shapes, move them, then drop them anywhere on the screen.
        I googled a while to find a perfect library for that, then I came across <a href="http://interactjs.io/" target="blank">Inreact.js</a>. It provides drag and drop gestures for modern browsers, precisely what I was looking for.
      </p>

      <p class="text">
        I used jsDelivr CDN to enable the dependencies. I chose only the dragging module, on which I needed just two little modifications.
        Instead of the parent element I marked the "body" as restriction area. As a result, I could move the shapes to every part of the Home page. Then I switched endOnly to "true", after that the dragged shape got stuck when the closest side met the edge of the screen. Then I only needed to add a "draggable" class to every item I wanted to move.
